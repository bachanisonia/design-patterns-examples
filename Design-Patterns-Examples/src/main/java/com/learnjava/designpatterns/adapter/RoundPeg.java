package com.learnjava.designpatterns.adapter;

public class RoundPeg {

	private int radius;
	
	public RoundPeg() {
		
	}
	
	public RoundPeg(int radius) {
		this.radius = radius;
	}

	public int getRadius() {
		return radius;
	}

	public void setRadius(int radius) {
		this.radius = radius;
	}
	
}


